                        <h3 class='mt-4'>Cancer Tests</h3>
                        <p>At CGPL we want to give you peace of mind - checking your body for cancer before you have symptoms. Getting screening tests regularly may find breast, cervical, and colorectal (colon) cancers early, when treatment is likely to work best. Lung cancer screening is recommended for some people who are at high risk.</p>

<p>Our Specialist Consultants and Private GPs offer cancer tests and cancer screening to include:</p>

<ul>
	<li><a href="cancer/psa-prostate-cancer-test.php">PSA &ndash; Prostate Cancer test</a></li>
	<li><a href="cancer/cervical-smear-with-hpv-cervical-cancer-test.php">Cervical Smear with HPV &ndash; Cervical Cancer test</a></li>
	<li><a href="cancer/early-cdt-lung.php">Early CDT Lung testing &ndash; Lung Cancer Blood Marker</a></li>
	<li><a href="cancer/ca-125-ovarian-cancer-blood-test.php">CA 125 &ndash; Ovarian Cancer blood test</a></li>
	<li><a href="cancer/he4-roma.php">HE4 &amp; ROMA</a></li>
	<li><a href="../consultants/breast-surgeon/breast-cancer-gene-testing.php">BRCA</a></li>
</ul>

<p><strong>PSA</strong></p>

<p>There&#39;s currently no screening programme for prostate cancer in the UK. This is because it has not been proved that the benefits would outweigh the risks.</p>

<p>Instead of a national screening programme, there is an informed choice programme, called prostate cancer risk management, for healthy men aged 50 or over who ask their GP about PSA testing. It aims to give men good information on the pros and cons of a PSA test.</p>

<p>If you&#39;re a man aged 50 or over and decide to have your PSA levels tested you can have this at CGPL.</p>

<p>If results show you have a raised level of PSA, your GP may suggest further tests.</p>

<p>The cost of a consultation is &pound;95.00</p>

<p>The cost of a PSA test is &pound;120.00</p>

<p><strong>Cervical Cancer</strong></p>

<p>The Pap test ( cervical smear ) can find abnormal cells in the cervix which may turn into cancer. The HPV test looks for the virus (human papillomavirus).</p>

<p>The guidelines around cervical cancer have changed. HPV is now seen as even more useful in itself re cervical cancer risk than abnormal cells. This is why - at CGPL we test our clients for HPV as well as endocervical cancer (smear test).</p>

<p>Cervical Smear/HPV/Exam + Consultation with our Gynaecologist - &pound;325.00</p>

<p>HPV testing is felt to be better at predicting cervical cancer risk - see this link</p>

<p>In some areas, a test for HPV is the first test on the screening sample. This is called primary HPV screening. In these cases, the sample is only checked for abnormal cells if HPV is found.</p>

<p><strong>Early CDT Lung</strong></p>

<p>You should consider a Lung Health Screen if you have any of the following risk factors:</p>

<ul>
	<li>You are a heavy smoker, especially of cigarettes, cigars, shisha and marijuana</li>
	<li>Prolonged exposure to second-hand smoking</li>
	<li>You have close relatives who have or previously had lung cancer</li>
	<li>Exposure to radon, asbestos, coal products and/or radioactive substances</li>
	<li>Exposure to chemicals such is arsenic, vinyl chloride, and other carcinogens</li>
	<li>Diagnosis of tuberculosis, silicosis and/or berylliosis with or without scarring on the lungs</li>
	<li>Recurring pneumonia or bronchitis</li>
</ul>

<p>The EarlyCDT&reg;-Lung is a simple blood test to aid in the risk assessment and early detection of lung cancer. This test detects circulating antibodies against the cancer cells in the blood. Antibodies can be detected well before patient become symptomatic. The turnaround time for results is approximately 10 days.<br />
&nbsp;<br />
EarlyCDT-Lung has been developed so that individuals at moderate or high risk of developing lung cancer have more chance to&nbsp;benefit from detection at the earliest possible stages&nbsp;when treatment can be most successful.</p>

<p>There are three possible test results; High Level, Moderate Level and Low Level.</p>

<p>A consultation with the GP costs &pound;95.00</p>

<p>The early CDT Lung test costs &pound;154.00</p>

<p><strong>CA 125</strong></p>

<p>This ovarian cancer screening test costs &pound;130. This test can be performed within a GP consultation which costs &pound;95.00</p>

<p>Our patients can also have this test with our Consultant Gynaecologist which costs &pound;225.00 + the cost of the test - &pound;130.00</p>

<p><strong>HE4 ROMA</strong></p>

<p>You may also wish to consider the CA125, HE4 ROMA combined test. This is more expensive but it tests for CA125 and combines this with HE4 which significantly raises the sensitivity when detecting ovarian cancers. By using both the CA125 and HE4 results, ROMA (A risk of ovarian malignancy algorithm) is calculated to show whether you are at high or low risk.</p>

<p>This test costs &pound;160&nbsp;in addition to the consultation with the Gynaecologist.</p>
