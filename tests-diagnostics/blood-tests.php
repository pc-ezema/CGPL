                        <h3 class='mt-4'>Blood Tests</h3>
                        <p><strong>CGPL offers same day access to give you peace of mind and help you resolve health problems quickly. This often requires diagnostic tests like blood and urine analysis.</strong></p>

<p>Unfortunately, NHS waiting times for these procedures can be long and just add to your worry and anxiety.</p>

<p>We offer a range of specific pathology tests at our&nbsp;Sheffield  clinic, that can take place quickly with results and reports returned promptly.</p>

<p>There is no need to get a referral from your NHS GP.</p>

<p><strong>Fast-tracked results</strong></p>

<p>We work with pathology services (laboratory tests) to find out if you have conditions like diabetes or an overactive/underactive thyroid. For your peace of mind, we&rsquo;ll get your results back to you as soon as possible.</p>

<p>&nbsp;</p>

<p>Blood testing is one of the quickest, easiest, and least invasive methods to identify any issues or diagnose a number of health problems &ndash; from an infection to screening for a genetic condition or cancer.</p>

<p>We are able to offer a range of quality accredited pathology services with access to over 1,500 tests on blood, urine and other samples. Our tests are available whether you are feeling unwell, or want specific screening for diabetes, liver function, hormones, allergies, cancer markers or otherwise.</p>

<p>Blood testing services are available to all members of the public on a self-paying basis with just a simple call and booking, without any need for a referral.</p>

<p>Consultations with our GPs cost &pound;95.00</p>

<p>You also have the option of booking a blood test with our HCAs which costs &pound;25.00</p>

<p>We also offer a GP service following your test, should you wish to discuss the outcome of your results for any further investigations needed.</p>

