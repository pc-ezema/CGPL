<div>
<div>
<div>
<p>Testosterone is the main sex&nbsp;hormone&nbsp;in males. Women also have testosterone in their bodies, but in much smaller amounts.</p>

<p>This test measures the levels of testosterone in your blood. Most of the testosterone in the blood is attached to proteins. Testosterone that is not attached to a protein is called free testosterone. There are two main types of testosterone tests:</p>

<ul>
	<li><strong>Total testosterone,</strong>&nbsp;which measures both attached and free testosterone.</li>
	<li><strong>Free testosterone,</strong>&nbsp;which measures just free testosterone. Free testosterone can give CGPL Clinicians more information about certain medical conditions.</li>
</ul>

<p>Testosterone levels that are too low (low T) or too high (high T) can cause health problems in both men and women.</p>

<p>Our Private GPs and Consultant Urologists regularly test Testosterone.</p>

<p>If you choose to have the test with the HCA you will need to have a consultation with the GP or Consultant to have the results explained to you.</p>

<div>
<p><strong>What is it used for?</strong></p>

<p>A testosterone levels test may be used to diagnose several conditions, including:</p>

<ul>
	<li>Decreased sex drive in men and women</li>
	<li>Infertility&nbsp;in men and women</li>
	<li>Erectile dysfunction&nbsp;in men</li>
	<li>Tumors of testicles&nbsp;in men</li>
	<li>Early or delayed puberty in boys</li>
	<li>Excess body hair growth and development of masculine features in women</li>
	<li>Irregular&nbsp;menstrual periods&nbsp;in women</li>
</ul>
</div>

<div>
<p>You may need this test if you have symptoms of abnormal testosterone levels. For adult men, it&#39;s mostly ordered if there are symptoms of low T levels. For women, it&#39;s mostly ordered if there are symptoms of high T levels.</p>

<p>Symptoms of low T levels in men include:</p>

<ul>
	<li>Low sex drive</li>
	<li>Difficulty getting an erection</li>
	<li>Development of breast tissue</li>
	<li>Fertility problems</li>
	<li>Hair loss</li>
	<li>Weak bones</li>
	<li>Loss of muscle mass</li>
</ul>

<p>Symptoms of high T levels in women include:</p>

<ul>
	<li>Excess body and facial hair growth</li>
	<li>Deepening of voice</li>
	<li>Menstrual irregularities</li>
	<li>Acne</li>
	<li>Weight gain</li>
</ul>
</div>
