      <h3>Midlands Ultrasound and Medical Services cancellation and rescheduling policy</h3>

<p>&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; We cannot refund vaccinations once they have been administered</p>

<p>&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; We reserve the right to refuse service and or treat patients who refuse to share the details of their NHS GP</p>

<h3>Pre-payment policy</h3>

<p>We may require pre-payment/deposit to secure your appointment with a consultant.</p>

<p>Previous patients who have failed to attend an appointment will be subject to full payment in advance to secure an appointment.</p>

<p>The remainder of your consultation fee plus any additional tests, medications or service(s) is required to be paid in full at the end of your consultation &ndash; when checking out at reception</p>

<h3>Cancellation Policy</h3>

<p>All specialist consultant bookings will incur a &pound;50.00 administration fee for any cancelled appointments if less than 24 hours notice is given or the patient does not attend. The rest will be refunded back to original payment method.</p>

<p>Appointments bookings can be cancelled if more than 24 hours&rsquo; notice is given and a full refund issued.</p>

<h3>Promotional rates/ Pricing</h3>

<p>Appointments booked during a promotional period which are rescheduled, or cancelled and subsequently rebooked, will not be eligible for any promotional rate if the promotion has since closed.</p>

<p>All appointments costs are charged at the prevailing rates at the time of last booking. Appointments rescheduled or cancelled and rebooked are charged at the rates on the date of last booking</p>

<h3>Late Policy</h3>

<p>To provide the best possible service to all our patients, we ask you to contact us if you are running late so we can make all necessary arrangements to ensure you are seen by our Consultants and GPs. As our clinics are very busy, you may be asked to reschedule your appointment or wait for an available slot so that all our patients receive the best possible service.</p>

<p>If you are more than 10 minutes late for your appointment without our prior agreement, we may consider you to have failed to attend, in which case, you will not be eligible for a refund of your pre-payment/deposit.</p>

<h3>Failure to attend</h3>

<p>If you fail to attend your appointment or are more than 10 minutes late to your booked appointment or cancel your appointment within 4 hours of the scheduled start date you will be deemed to have missed your appointment. Our team will contact you to help you reschedule your appointment on one occasion. You will not be eligible for a refund of any pre-payment or deposit if you fail to attend on the rescheduled appointment.</p>

<h3>Refunds</h3>

<p>Refunds for eligible pre-payments or deposits will only be made to the card originally used to make the original booking. Refunds should appear within your account or on your card statement within 7 days.</p>

<p>We cannot refund any medications, vaccinations, tests or treatments once they have been administered or dispensed.</p>

<p>Refunds are not eligible for services that were carried out.</p>

<p>The Covid Day 2 &amp; Day 8 Testing are also not eligible for a refund once the Unique Reference Number (URN) has been issued by CGPL. Appointments can be re-arranged however.</p>

<h3>Non-GP Services&nbsp;</h3>

<p>CGPL works in collaboration with consultants, consultant groups, diagnostic facilities and intermediary parties/brokers to ensure the provision of continuity of care to each patient. These collaborations entail a commercial relationship between the parties whereby a percentage of the fee paid by patient for the service is shared between CGPL and the relevant party.</p>

<h3>Pre-payment policy</h3>

<p>We require pre-payment/deposit to secure the booking of appointments with our specialists and Consultants.</p>

<p>We will contact you before your consultation to confirm attendance and take the pre-payment/deposit. Any additional tests, medications or service(s) will be paid in full at the end of your consultation. If you do not pre-pay for your consultation at least 48 hours before its start time, your appointment will be deemed as unconfirmed and cancelled as a result.</p>

<p>We cannot refund any medication, vaccinations or treatments once they have been administered or dispensed.</p>

<h3>Privacy</h3>

<p>By accepting these Terms and Conditions, you acknowledge that you understand, have read and will abide by CGPL Patient Privacy Policy which is part of these Terms and Conditions by reference.</p>

<h3>Applicable law:</h3>

<p>These Terms and Conditions are governed by English law and you can bring legal proceedings in respect of our Services in the English courts. If you live in Scotland, you can bring legal proceedings in respect of the products in either the Scottish or the English courts. If you live in Northern Ireland, you can bring legal proceedings in respect of the products in either the Northern Irish or the English courts.</p>

<h3>Notices:</h3>

<p>CGPL may contact you by email to the email address provided in your account, by written communication to the postal address provided in your account, SMS or by a general notice on the system. As per GDPR regulations we always check communication preferences at the time of booking a new patient. If you wish to update these preferences then one of the team can assist you with this please email&nbsp;us or call us.</p>

