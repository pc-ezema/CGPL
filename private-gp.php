&nbsp;</p>

<h2 dir="ltr">What Services Do Our Private GPs Offer?</h2>

<p dir="ltr">Here at MUMs, our private GPs offer the same services as NHS GPs.&nbsp; However, you will never be made to wait for an appointment, nor will your appointment be rushed.&nbsp;</p>

<p dir="ltr">Here are just some of the services that our private GPs offer:</p>

<ul dir="ltr">
	<li><a href="private-gp/consultations.php">Consultations</a></li>
	<li><a href="private-gp/health-screens.php">Health screens</a></li>
	<li><a href="private-gp/hayfever-injection.php">Hay Fever injections</a></li>
	<li><a href="private-gp/immunisations-and-travel-vaccinations.php">Immunisations and travel vaccinations</a></li>
