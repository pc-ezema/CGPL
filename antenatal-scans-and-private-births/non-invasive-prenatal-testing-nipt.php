                        <h3 class='mt-4'>Accurate Prenatal Testing</h3>
                        <p>MUMS offer two NIPT options.</p>

<ol>
	<li>The Harmony Test</li>
	<li>Panorama Test</li>
</ol>

<p>Screening with these tests identifies more than 99% of pregnancies with Down syndrome, whereas traditional screening can miss as many as 20% (1 in 5).</p>

<p>The NIPT test performance is superior to traditional screening for Down syndrome.</p>

<p>Harmony and Panorama are non-invasive blood tests that screen for specific chromosome conditions in a pregnancy as early as 9 weeks for Panorama and 10 weeks for Harmony.</p>

<p>When you&rsquo;re pregnant, your blood contains tiny amounts of your baby&rsquo;s DNA. The prenatal test looks at this DNA to provide accurate information about the likelihood for the most common chromosome conditions such as Down syndrome (trisomy 21), Edwards syndrome (trisomy 18), and Patau syndrome (trisomy 13).</p>

<p>This test includes the following information for you which you may or may not want to know so you can decide at the time of the test which information you are interested in.</p>

<ul>
	<li>Monosomy X &ndash; Also called Turner syndrome, a condition where a female is missing an X chromosome, and as a result, may have heart, endocrine, and learning problems</li>
	<li>DiGeorge syndrome (22q11.2 deletion syndrome) &ndash; A condition caused by a small missing piece (&lsquo;deletion&rsquo;) of chromosome 22, which can result in heart, kidney, learning, and growth problems - (Only available through Panorama testing)</li>
	<li>Sex Chromosome Aneuploidy &ndash; Differences in the number of X and Y chromosomes which can result in certain learning and/or behavioral challenges in addition to other health issues.</li>
	<li>Fetal sex &ndash; So you know whether your baby is male or female (also available with twins)&nbsp;&ndash;&nbsp;In twins the fetal sexing identifies the presence of male DNA so if no male DNA is present both fetuses are presumed female. If male DNA is present the test cannot distinguish between 1 male &amp; 1 female or 2 male fetuses.</li>
</ul>

<p>Chromosomal conditions like Down syndrome do not typically run in families and can happen in any pregnancy. Although the chance of having a baby with Down syndrome increases with age, most babies with Down syndrome are born to women under 35.</p>

<p>The Harmony and Panorama tests are a cell-free DNA-based prenatal blood screen. The test can be used in singleton, twin, and egg-donor pregnancies and has been validated for use in pregnant women aged 18 to 48. It can be administered as early as 10 weeks gestation.</p>

<p>The test can screen for:</p>

<ul>
	<li>Trisomies 21, 18, and 13</li>
	<li>Sex chromosome aneuploidy</li>
	<li>Monosomy X</li>
	<li>22q11.2 deletion (Only available through Panorama testing)</li>
	<li>Fetal sex</li>
</ul>

<p><strong>Panorama Test</strong></p>

<p>An extended panel of testing is available with Panorama.</p>

<p>Non-invasive prenatal testing (NIPT) analyses cell-free DNA circulating in a pregnant mother&rsquo;s blood. It is used screen for Down syndrome (trisomy 21) and other common chromosomal conditions (trisomies 18 and 13). Options are also available to screen for X and Y chromosome conditions or for a deletion in chromosome 22q11.2 and other chromosomes, such as:</p>

<ul>
	<li>1p36 deletion syndrome</li>
	<li>Prader Willi syndrome</li>
	<li>Angelman syndrome</li>
	<li>Cri-du-chat syndrome</li>
</ul>

<p><strong>Please note that for Twin/Donor/Surrogacy pregnancies the extended panel/microdeletions<span style="font-size: 10pt; line-height: 107%; font-family: Arial, sans-serif; background-image: initial; background-position: initial; background-size: initial; background-repeat: initial; background-attachment: initial; background-origin: initial; background-clip: initial;">&nbsp;</span></strong><strong>cannot be performed.</strong></p>

<p><strong>About the test</strong></p>

<p>DNA from the fetus circulates in the mother&rsquo;s blood. Cell-free DNA (cfDNA) results from the natural breakdown of fetal cells (presumed to be mostly placental) and clears from the maternal system within hours of giving birth.&nbsp;</p>

<p>During a pregnancy, cfDNA can be tested to give the most accurate screening approach in estimating the risk of a fetus having a common chromosome condition sometimes called a trisomy. This occurs when there are three copies of a particular chromosome instead of the expected two. The test looks to detect the following conditions:</p>

<p>Trisomy 21&nbsp;is the most common trisomy at the time of birth. Also called Down syndrome, it is associated with moderate to severe intellectual disabilities and may also lead to digestive disease, congenital heart defects and other malformations.</p>

<p>Trisomy 18&nbsp;(Edwards syndrome) and&nbsp;Trisomy 13&nbsp;(Patau syndrome) are associated with a high rate of miscarriage. These babies are born with severe brain abnormalities and often have congenital heart defects as well as other birth defects. Most affected individuals die before or soon after birth, and very few survive beyond the first year of life.</p>

<p>Sex chromosome conditions&nbsp;occur when there is a missing, extra, or incomplete copy of the X or Y chromosomes. Both Panorama and Harmony tests with sex chromosome aneuploidy panel option can assess risk for XXX, XYY, XXYY, XXY (Klinefelter syndrome), and a missing X chromosome in a girl (Turner syndrome).</p>

<p><strong>Risk</strong></p>

<p>The testing is non-invasive: it involves taking a blood sample from the mother. The pregnancy is not put at risk of miscarriage, or from other adverse outcomes that are associated with invasive testing procedures such as amniocentesis.</p>

<p>There is a 3% chance that we might not get a result and a repeat sample will be required. This is due to lack of fetal DNA. Two samples are sent to the laboratory so in the event of the first sample failing, a 2nd back up test can be analysed. In rare cases, both samples fail. We offer a repeat test free of charge.</p>

<p><strong>Who can have this test?</strong></p>

<p>Women who are at least 9 weeks can have Panorama.<br />
Women who are at least 10 weeks can have Harmony.</p>

<p>These tests can be requested for any singleton or twin pregnancy, including those conceived naturally or by IVF using the patient&rsquo;s own egg or a donor egg. Note that, in twin pregnancies, sex chromosome (X and Y) analysis can determine fetal sex but not sex chromosome conditions. The Harmony test also does not assess risk for mosaicism, partial trisomies or translocations.</p>

<p><strong>Results</strong></p>

<p>Harmony &ndash; 10&nbsp;days from arriving at the lab in London<br />
Panorama &ndash; 10 days from arriving at the lab in California</p>

<p>Women still can have their 12-week scan for a detailed examination of the fetal anatomy, including measurement of nuchal translucency, nasal bone and other important factors. In this visit, patients can discuss the DNA and ultrasound results with their obstetricians.</p>

<p>The non-invasive prenatal test does not provide information on other physical defects such as spina bifida, or information on fetal growth. It is therefore advisable that the mother has all the usual ultrasound scans during her pregnancy.</p>

<p>The choice is yours.</p>
