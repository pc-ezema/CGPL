      <h3>Protecting your privacy</h3>

<p>We are committed to protecting your privacy when dealing with your personal information. The following information tells you about how we collect your data, how we use it and how we protect it. It also provides you with your rights. If you have any questions about how we handle your data, please feel free to contact us at comfortcaregroups.co.uk </p>

<h3>Patient Privacy Notice</h3>

<p>Key points:</p>

<p><strong>Why do we use your data?</strong></p>

<p>We use information about you for the purpose of providing you with our healthcare services.</p>

<p>&nbsp;</p>

<p>We use your sensitive data - As a patient, we will use information about your health, racial and ethnic origin, sexual orientation and religion.</p>

<p>Sharing data - We may share your data with third parties, including other healthcare providers who may contribute to your care, your GP, test labs, statutory bodies concerned with the regulation of your care, and other agencies who may be involved in your care.</p>

<p>Security - We respect the security of your data and treat it in accordance with the law. International transfer: We work with international medical providers where we may transfer data with the patient&rsquo;s express consent.</p>

<p>Comfortcare Group of Partnership Limited respects your privacy and is committed to protecting your personal data. This privacy notice will inform you as to how we look after patient&rsquo;s personal data and tell you about your privacy rights and how the law protects you.</p>

<p>Please also use the Glossary to understand the meaning of some of the terms used in this privacy notice.</p>

<p>&nbsp;</p>

<h3>IMPORTANT INFORMATION AND WHO WE ARE</h3>

<p>Under the General Data Protection Regulation and Data Protection Act 2018, we are required to explain to our patients why we collect information about you, how we intend to use that information and whether we will share this information with anyone else.</p>

<p>&nbsp;</p>

<p>This statement applies to all our patients and also prospective patients.</p>

<p>&nbsp;</p>

<p>We may update this statement at any time, and we will provide you with a new privacy statement when we make any substantial updates. We may also notify you in other ways from time to time about the processing of your personal information.</p>

<p>&nbsp;</p>

<p>It is important that you read this statement so that you know how and why we use information about you. It is also important that you inform us of any changes to your personal information so that the information which we hold about you is accurate and up to date.</p>

<p>&nbsp;</p>

<p>We are Mildands Ultrasound and Medical Services and are a &ldquo;data controller&rdquo; for the information which we hold about you. This means that we are responsible for deciding how we hold and use the personal information which we hold about you.</p>

<p>&nbsp;</p>

<p>We have appointed a Data Protection Officer (&ldquo;DPO&rdquo;) who is responsible for overseeing what we do with your information and monitoring our compliance with data protection laws.</p>

<p>&nbsp;</p>

<p>You have the right to make a complaint at any time to the Information Commissioner&rsquo;s Office (ICO), the UK supervisory authority for data protection issues (www.ico.org.uk). We would, however, appreciate the chance to deal with your concerns before you approach the ICO so please contact us in the first instance.</p>

<p>CGPL (Midlands Ultrasound &amp; Medical Services (CGPL) Limited) is registered with the ICO, registration number Z8276804.</p>

<p>&nbsp;</p>

<h3>WHY ARE WE COLLECTING YOUR INFORMATION?</h3>

<p>We ask for information about you so that we can make sure we provide you with the best healthcare care and service as our patient.</p>

<p>Where we need to collect personal data by law, or under the terms of a contract we have with you and you fail to provide that data when requested, we may not be able to provide you with the service you require, but we will notify you if this is the case at the time.</p>

<p>&nbsp;</p>

<h3>TYPES OF PERSONAL INFORMATION WE USE</h3>

<p>&nbsp;We are collecting information about you which is relevant to your care with us and the care which we provide to you.</p>

<p>&nbsp;</p>

<p>This includes:</p>

<p>&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; personal details (such as name, date of birth, gender, marital status, national insurance number, occupation)</p>

<p>&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; contact details (such as your address, personal telephone numbers and personal email address)</p>

<p>&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; financial information (such as your bank account details and information about your financial circumstances)</p>

<p>&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; information about your next of kin (for the purpose of emergency contact details)</p>

<p>&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; information about other healthcare providers (such as your GP and practice details)</p>

<p>&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; video and photographs of you (such as CCTV footage, photographs for security purposes, etc)</p>

<p>&nbsp;</p>

<h3>SPECIAL CATEGORIES OF PERSONAL DATA</h3>

<p>Some of the information which we collect about you may be &ldquo;special categories of personal data&rdquo;. Special categories of data require a greater level of protection. The special categories of personal data about you which we may collect include:</p>

<p>&nbsp;</p>

<p>&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; information about your racial or ethnic origin;</p>

<p>&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; information about your religious beliefs;</p>

<p>&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; information about your sex life and sexual orientation;</p>

<p>&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; healthcare information, including:</p>

<p>&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; any disabilities or special requirements which you may have;</p>

<p>&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; medical records relating to your treatment by us;</p>

<p>&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; your medical history and records required by care regulators.</p>

<p>&nbsp;</p>

<h3>AGGREGATED DATA</h3>

<p>We also collect, use and share anonymised and aggregated data (&ldquo;Aggregated Data&rdquo;) such as statistical or demographic data for our own internal and marketing purposes. Aggregated Data is be derived from your personal data but is not considered personal data in law as this data does not directly or indirectly reveal your identity. However, if we combine or connect Aggregated Data with your personal data so that it can directly or indirectly identify you, we treat the combined data as personal data which will be used in accordance with this privacy notice.</p>

<p>&nbsp;</p>

<h3>SOURCE OF YOUR PERSONAL INFORMATION</h3>

<p>The above information which we collect about you will be obtained through a variety of sources which include:</p>

<p>&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; from you directly both prior to and during the course of your treatment by us</p>

<p>&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; from your friends and relatives who provide us with information about you</p>

<p>&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; from other healthcare professionals and officers in the local authority/ social services department.</p>

