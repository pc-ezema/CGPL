                        <h3 class='mt-4'>Consultant Breast Surgeon</h3>
                        <p><img alt="" src="../images/images/seni-mylvaganam-consultant-breast-surgeon.jpg" /></p>

<p dir="ltr">Mr Seni Mylvaganam is CGPL&nbsp;breast consultant. Based in our clinic, Seni is a Consultant Oncoplastic Breast Surgeon who specialises in the surgical assessment and management of breast symptoms and benign disease, breast cancer and breast reconstruction. He also has expertise in the practice of cosmetic breast surgery.</p>

<p>A member of the Association of Breast Surgery Clinical Practice and Standards Committee, Seni initially qualified as a doctor in 2003 at the Royal Free and University College London Medical School before completing his bachelor&rsquo;s degree in Pharmacology at University College London. After completing his medical degree, he completed his specialist training with the National Oncoplastic Breast fellowship. The fellowship is restricted by competition to select breast and plastic surgeons each year. It is regarded as the gold standard for breast reconstructive training in the UK.</p>

<p>After completing his specialist registrar training, he was appointed as Consultant Oncoplastic Breast Surgeon at the Royal Wolverhampton Hospitals NHS Trust. He also holds the role of professional clinical advisor in surgery to the NHS Breast screening programme. He is a member of the Association of Breast Surgery Clinical Practice and Standards Committee, part of the board responsible for UK national breast surgery practice standards.</p>

<p>Mr Seni Mylvaganam aims to provide every patient with the highest quality assessment, advice, and treatment, whether this is regarding benign breast symptoms, breast cancer care or cosmetic considerations. His patients remain at the heart of his work, and he states that &quot;understanding their needs is essential to offering the best treatment tailored to each individual.&quot;.</p>

<p>In the United Kingdom, breast surgery is one of the most common plastic surgery procedures carried out. Each year, approximately 7,727 breast enlargements take place. Meanwhile, 4,014 breast reductions are conducted. Here at CGPL, we offer breast surgery consultant consultations and breast cancer risk screenings to anyone that requires them.&nbsp; Find out more about our services here or contact us directly for our expert advice and guidance.</p>

<p>&nbsp;</p>

<h2 dir="ltr">Breast Surgery Consultations</h2>

<p>While breast surgery consultations must be provided before cosmetic breast surgery procedures are completed, breast surgery consultations are additionally required to manage and investigate symptoms associated with breast disease, cancer, and reconstructions. At CGPL, our breast surgery consultations see our breast surgery consultants review our client&#39;s general health, pre-existing health conditions, risk factors and breasts before recommending a suitable course of action. If breast surgery consultations are required to detect breast cancer or the risk of breast cancer, our consultant can conduct breast cancer risk screening and tests.</p>

<p>&nbsp;</p>

<h2 dir="ltr">Breast Cancer Risk Screening</h2>

<p>Breast surgery consultations, as noted above, are often required to detect breast cancer and the risk of breast cancer. If a patient wants to undergo these tests, our breast surgery consultant is on hand to conduct them. Though the test completed will depend on various factors, such as whether breast cancer has previously been experienced, we offer two different gene tests at our clinic. These tests include Invitae BRCA Breast Cancer STAT Panel and AnteBC Test.</p>

<p dir="ltr">The two tests provide different information. As a result, they can be used together. Upon arranging a consultation with our breast surgery consultant, you will be guided through which tests are recommended for you. To provide greater insight into the two tests we offer, we have provided a wealth of information below.</p>

<p dir="ltr">The aim of a breast reassurance clinic is to provide women with fast direct access to an expert breast specialist to evaluate their symptoms and provide a prompt, clear, detailed explanation and diagnosis for each woman. Find out more about our <a href="breast-surgeon/breast-cancer-ultrasound-testing.php">breast cancer ultrasound scan tests</a> here.</p>

<p>Genomic medicine (the use of a person&rsquo;s individual DNA to inform about disease risk and how to treat) is increasingly playing a central part in how we view and treat breast cancer.&nbsp; It can enable us to identify individuals at a higher risk of developing breast cancer so a person can know their risk but also what to do with that information to reduce their risk. Learn more about our <a href="breast-surgeon/breast-cancer-gene-testing.php">Breast Cancer gene testing</a> here.</p>

<p>&nbsp;</p>

<h3 dir="ltr">Invitae BRCA STAT Panel</h3>

<p>Some specific rare gene defects can significantly increase an individual&#39;s risk of developing breast cancer. To identify the risk of breast cancer, our breast surgery consultations provide Invitae BRCA STAT Panel screening to determine whether the BRCA 1 and BRCA 2 genes are present. If an individual has either of the genes associated with breast cancer, breast surgery consultations are provided to discuss what this means and how this can be managed.</p>

<p>This test is best for women who have:</p>

<p dir="ltr">1. &nbsp; &nbsp; &nbsp;&nbsp; &nbsp;Had breast cancer and want to know if they are at extra risk of developing breast cancer in the other breast</p>

<p dir="ltr">2. &nbsp; &nbsp; &nbsp;&nbsp; &nbsp;Have a strong family history of breast cancer, making it a little more likely they may have one of these nine gene defects in their DNA</p>

<p dir="ltr">3. &nbsp; &nbsp; &nbsp;&nbsp; &nbsp;In combination with the AnteBC Test in those women with a strong or moderate family history of breast cancer, to give them an individual risk of developing breast cancer.</p>

<p>&nbsp;</p>

<h3 dir="ltr">AnteBC Test</h3>

<p>Recommended to women between the ages of 30 and 75, the AnteBC test determines the risk of developing breast cancer.&nbsp;</p>

<p dir="ltr">Reviewing DNA for any specific variations associated with breast cancer, this AnteBC test is suitable for women who do not have a family history of breast cancer but wish to know their risk of developing breast cancer at some point in their life. Based on any variations identified, the AnteBC test produces a score that outlines each individual&#39;s risk of developing breast cancer in the next ten years compared to the average woman of their age in the population.</p>

<p dir="ltr">This score does not ascertain whether breast cancer will be experienced. Instead, it describes an individual&#39;s chance of developing breast cancer. It should also be noted that this score does not determine whether relatives are at increased risk. The risk score is also not something that can be passed onto children. Conducted by a breast surgery consultant, consultations combine test results and risk factors to provide clients with a bespoke guide.</p>

<p dir="ltr">This test is best for women who:</p>

<p dir="ltr">1. &nbsp; &nbsp; &nbsp;&nbsp; &nbsp;Do not have a strong family history of breast cancer but still want to know their risk of breast cancer.</p>

<p>&nbsp;</p>

