                        <h3 class='mt-4'></h3>
                        <p>An electrocardiogram (ECG) is used to record the heart&rsquo;s rhythm and electrical activity. The purpose of an ECG is to find out if the heart is healthy by detecting irregular heart rhythms and to help diagnose the cause of symptoms such as chest pain or palpitations.</p>

<p>Signals recorded from your heart are displayed on a screen and can be traced out on a piece of paper.</p>

<p>An ECG can identify problems with your heart including:</p>

<ul>
	<li>A heart attack</li>
	<li>An enlarged heart that is working under strain</li>
	<li>Fast, slow or irregular heartbeats called arrhythmias</li>
</ul>

<p>An ECG can also help investigate the cause of chest pains, palpitations, or symptoms such as difficulty in breathing.</p>

<p>It is also used to assess if a patient has had a heart attack or to look for evidence of a previous heart attack.</p>

<p>The heart&rsquo;s electrical activity is measured through electrodes which are attached to the skin with adhesive pads. These electrodes measure the electrical signals of the heart. A standard ECG is a very simple and quick procedure that is completely painless.</p>

<p>It is advisable to shave the chest area prior to ECG as we use sticky pads that need to adhere to the skin.</p>

<p>We can offer you an ECG with our Health Care Assistant &ndash; the cost is &pound;25.00 for the appointment and &pound;50.00 for the ECG.</p>

<p>You may prefer a GP Consultation which costs &pound;85.00 plus the cost of the ECG which is &pound;50.00</p>

<p>Alternatively you may wish for the Cardiologist to perform your ECG which costs &pound;230.00 plus &pound;50.00 for the ECG.</p>
