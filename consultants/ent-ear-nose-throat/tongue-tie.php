                        <h3 class='mt-4'>Tongue Tie at MUMS</h3>
                        <p>At MUMS, we have two experienced ENT consultants who have&nbsp;a wealth of experience dealing with ENT issues, including tongue tie in babies. As our clinic is based in Sheffield , we are an ideal location for patients from across the .</p>

<p>Our ENT specialists offers baby tongue tie consultations where the baby will be examined to ascertain&nbsp;whether they have a tongue tie or not and can provide tongue tie release surgery if required at the time of the consultation.</p>

<p>Upon consultation, if the baby is identified to have a tongue tie, then the ENT specialist consultant will take the time to discuss the condition, and the potential correction procedure, with you and will answer any questions you may have. Our ENT consultant is able to perform the tongue tie correction surgery on the same day. Consent will be sought, and written information and aftercare information will be provided.</p>

<h2>What is Tongue Tie?</h2>

<p>A tongue tie is a common problem that is present at the child&rsquo;s birth. It affects the movement of the tongue and in some cases the baby&rsquo;s tongue tie can cause an issue with feeding.</p>

<p>Often, the tongue tie is noticed by the midwife at birth. On some occasions it may not be recognised until later.</p>

<p>In some cases, if it has been left untreated, the impaired tongue movements may also affect speech when they are older.</p>

<h2>Tongue Tie Symptoms</h2>

<p>If you are worried that your baby may have a tongue tie, and it was not noted at the birth, there are a few signs that you can look out for to determine if that is the case:</p>

<p><strong>Issues with Breastfeeding</strong></p>

<p>If your baby suffers from a tongue tie, you may notice that they have difficulty latching on and establishing breast feeding. The feeds themselves may be shortened and the mother may develop pain and discomfort.</p>

<p><strong>Difficulty in tongue movement</strong></p>

<p>The tongue tie affects tongue movement. A tongue-tied baby may not be able to stick their tongue out of their mouth beyond the gums.</p>

<p><strong>A notch at the tip of the tongue</strong></p>

<p>If you identify and heart-shaped or a V-shaped notch on your baby&rsquo;s tongue, then this may indicate a tongue tie.</p>

<p>If you identify any of these symptoms and your baby is struggling to feed naturally, our experienced ENT consultant at MUMS can offer a tongue tie consultation and tongue tie surgery.</p>

<h2>Tongue Tie Assessment</h2>

<p>Our ENT Surgeon will be able to carry out an assessment of your baby with a suspected tongue tie. She will be able to carry out a tongue tie division procedure on the same day in the MUMS clinic. This is a surgical procedure which is carried out in a sterile and safe environment with an experienced consultant.</p>

<p>Our ENT Surgeons offer anterior (front) tongue tie correction at MUMS.</p>

<p>Although our ENT Surgeons specialise in posterior (back) tongue tie correction &ndash; this cannot be performed at MUMS &ndash; although this can be assessed at MUMS which costs &pound;50.00.</p>

<p>After the tongue tie is a divided the child is encouraged to feed. Our ENT consultant will then check the tongue tie site prior to discharging you home from our clinic.</p>

<h2>Tongue Tie Surgery</h2>

<p>If your baby is tongue-tied but can still feed without any problems, no treatment is needed. They will simply grow out of it. However, when the baby&rsquo;s feeding ability is impaired, tongue tie surgery may&nbsp;be required.</p>

<p>The prospect of your baby undergoing surgery may sound daunting; however, tongue tie surgery, also called tongue tie division, is a quick and simple procedure. The process involves cutting the piece of tissue (Frenulum), using a pair of sterile surgical scissors, that attaches the tongue to the bottom of the mouth to free the tongue&rsquo;s movements. It only takes a few seconds to perform in the hands of an experience surgeon and you can start feeding your baby immediately after.</p>

<p>If you have and concerns or questions our ENT specialist at MUMS will be happy to answer your queries.</p>

<p>Babies tolerate this procedure very well.</p>

<p>The cost of the consultation and surgery is &pound;175.00. We take a &pound;50.00 deposit to secure your&nbsp;consultation. If the procedure does not go ahead the further &pound;125.00 will not need to be paid. We will use the deposit for the consultation.</p>

