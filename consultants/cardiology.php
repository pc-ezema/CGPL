Dr Jerome Ment</h3>

<p dir="ltr">Cardiology consultants across the country continue to provide life-saving treatments to patients who suffer from cardiovascular diseases and defects.&nbsp;&nbsp;</p>

<p dir="ltr">While heart problems and cardiovascular disease are commonly only believed to impair elderly patients, almost anyone can find their life damaged by cardiovascular disease.</p>

<p dir="ltr">If you experience any problems with your heart, such as palpitations, pressure, pain in the chest or shortness of breath and find yourself waiting for NHS treatment, here at MUMs, we can offer you a private cardiology consultation&nbsp;and ensure that tests are conducted to provide an almost instant diagnosis.</p>

<h3><big>Our Cardiologist Consultant</big></h3>

<p dir="ltr">Dr Jerome Ment is the consultant cardiologist here at MUMs.&nbsp;&nbsp;</p>

<p dir="ltr">Jerome specialises in interventional cardiology and Echocardiography and deals with coronary artery disease and heart failure.</p>

<p dir="ltr">Patients with palpitations, heart arrhythmias, blood pressure, family history of CHD, chest pain, risk factors, med reviews, angina, hypertension, heart murmurs, fainting, dizziness, menopausal cardiac issues, breathlessness, AF, cardiac monitoring can consult with Jerome.</p>

<h3 dir="ltr">Book A Heart Cardiology Consultation Today</h3>

<p dir="ltr">If you have experienced symptoms such as shortness of breath, irregular heartbeat, chest pains, increased heart rate, dizziness, or light-headedness, regardless of your age or personal circumstances, you must book a heart cardiology consultation today.</p>

<p dir="ltr">Rather than waiting for an NHS appointment to become available,&nbsp;<a href="../contact-us.php">contact us</a>&nbsp;to&nbsp;<a href="../request-an-appointment.php">book an appointment</a>&nbsp;at our private clinic today.</p>

<h3 dir="ltr"><big>Our Cardiology Tests</big></h3>

<p dir="ltr">At MUMs, our consultant cardiologists carry out a wealth of tests that investigate underlying cardiology problems.&nbsp;&nbsp;</p>

<p dir="ltr">Although many individuals overlook the signs and symptoms associated with heart problems, cardiology treatment must be sought as soon as possible.&nbsp;&nbsp;</p>

<p dir="ltr">Not only can cardiology check-ups detect underlying heart and cardiovascular problems, but they can essentially prevent heart attacks and treat diseases that could otherwise significantly reduce the quality of an individual&rsquo;s life.</p>

<p dir="ltr">If you are worried about the health of your heart, we provide several tests that ensure diagnosis can be made and treatment can be administered.</p>

