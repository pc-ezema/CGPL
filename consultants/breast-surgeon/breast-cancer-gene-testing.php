                        <h3 class='mt-4'>Breast Cancer Gene Testing</h3>
                        <p>Detailed below are the gene tests offered by MUMS. They have been chosen to provide the greatest options to all potential patients who will have differing reasons for wanting to know their individual risk of breast cancer.</p>

<h3>1. Invitae BRCA Breast Cancer STAT Panel</h3>

<p>Some specific rare gene defects can significantly increase your risk of developing breast cancer in your lifetime.&nbsp; 9 of the most common genes are looked for in this test which include BRCA 1 and BRCA 2.</p>

<p>This test identifies whether you have any one of these defects.&nbsp; If you have any of these gene defects your surgeon will then discuss with you what this means for your lifetime risk of breast cancer and how this should be managed.</p>

<p>&nbsp;<br />
This test is best used for women who have:</p>

<ol>
	<li>Had breast cancer and wanting to know if they are at extra risk of developing breast cancer in the other breast</li>
	<li>Have a strong family history of breast cancer making it a little more likely they may have one of these 9 gene defects in their DNA</li>
	<li>In combination with the AnteBC Test in those women with a strong or moderate family history of breast cancer to give them an individual risk of developing breast cancer.</li>
</ol>

<p>&nbsp;</p>

<h3>2. AnteBC Test</h3>

<p>This test looks at your individual DNA for any specific variations (2803) that can be associated with breast cancer.</p>

<p>Based on any variations identified it then produces a score which gives you an individual risk of developing breast cancer in the next 10 years compared to the average woman of your age in the population.</p>

<p>This test is recommended for women between the ages of&nbsp;30&mdash;75 of European origin.</p>

<p>This test&nbsp;cannot diagnose breast cancer&nbsp;only the risk of developing breast cancer.</p>

<p>Your risk score simply describes the chance that you might develop breast cancer and whether it is higher or lower than the general population.</p>

<p>Your score does not tell you whether your relatives are at increased risk.&nbsp; The risk score is also not something that can be passed onto children. Your children will have their own unique risk score from this test which is not related to your risk score.</p>

<p>This test is best used for women who:</p>

<ol>
	<li>do not have a strong family history of breast cancer but are still looking to know what their risk of breast cancer might be.</li>
</ol>

<p>This test does not specifically identify high risk gene changes for breast cancer such as BRCA 1 and BRCA 2.&nbsp; To specifically test for these genes MUMS offer a separate strong gene panel test (Invitae Breast Cancer STAT panel).</p>

<p>There are other risk factors such as lifestyle factors that can also increase your risk of breast cancer.&nbsp; &nbsp;Your breast surgeon will put together those risks to provide a bespoke guide to discuss with you.</p>

<p>&nbsp;</p>

<p>The two tests offered do not give the same information and can be used together to give a personal risk for each woman.&nbsp; Your breast surgeon will guide you through which tests are recommended for you.</p>
