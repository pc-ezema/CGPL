                        <h3 class='mt-4'>Haemorrhoid treatments from eXroid</h3>
                        <h3>Say goodbye to piles, quickly and easily.</h3>

<p>Over 90% of eXroid patients report they return to their normal activities on the same day.</p>

<p>eXroid takes a pride in having a personal approach to your haemorrhoid treatment and care and our expert patient care team can help you get the best advice and help you need, so you could be free from piles sooner than you thought.</p>

<p>The eXroid&reg; treatment is uniquely suitable for all 4 grades of haemorrhoids, so even if your GP can&rsquo;t refer you for NHS treatment, you can be treated by eXroid without the need to wait until things get worse. If it&rsquo;s bothering you enough, don&rsquo;t wait &ndash; get it sorted and get back to living well.</p>

<p>eXroid offers the lowest risk and most effective treatment for haemorrhoids in the UK. Many patients report minimal or no discomfort during and after the procedure, with only the proctoscope insertion (used in all haemorrhoid treatments) feeling uncomfortable. There is no need for anaesthetic, cutting, heat or stitching, meaning no time off work. Appointments (including diagnosis &amp; treatment) can take less than an hour, so you can fit your appointment around your life.</p>

<h3>How does it work?</h3>

<p>The eXroid procedure uses electrotherapy, which has been clinically researched for over 150 years and is exclusively used by eXroid in the UK.</p>

<p>It is non-surgical and is carried out in a clinic by a GMC registered consultant. There is no cutting, stitching or heat applied, unlike other available procedures. The low electrical current is delivered to the area with fewer nerves, thus making it non-painful for most. It stimulates a chemical reaction, which seals off the feeding blood vessels, resulting in the haemorrhoid being starved of its blood supply and shrinking accordingly, with associated symptom reduction.</p>

<p>The effect can be felt from immediately following the procedure and continues to work to close the vessels for up to 4 weeks. Our aftercare team will be in touch at this point to hear your progress, review your doctor notes and talk to you about the need for any further treatment.</p>

