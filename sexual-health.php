<p><img alt="" src="images/images/Hannah-Church.jpg" style="margin: 5px;" /></p>

<p>Dr Hannah Church is a consultant at The Coventry and Warwickshire Partnership Trust where she has regular NHS clinics. She is a member of the British Association of Sexual Health and HIV (BASHH), the British HIV Association (BHIVA), and the Faculty of Sexual and Reproductive Health (FSRH). She has been an elected member of the BASHH Board, and was recently on the writing group for the BASHH National Sexual Health History taking guidelines.</p>

<p>If you are having problems with your sexual health or just want some peace of mind, then Dr Church can assess and diagnose your condition as well as advising you on the best possible treatments. You do not require a GP referral and we can often offer you a same day appointment.</p>

<p>Consultants in Sexual health and HIV Medicine specialise in can:</p>

<ul style="list-style-type:circle;">
	<li>Advise on your personal best screening tests&nbsp;for <u>Sexually Transmitted Infections (STIs)</u></li>
	<li>Give the best treatment for <u>STI positive results</u> or if you have been in contact with an STI</li>
	<li>Advise and manage genital non sexually acquired infection</li>
	<li>Advise on vaccinations that may be appropriate based on your sexual history</li>
	<li>Discuss and manage HIV acquisition risk and prescription of Pre-Exposure Prophylaxis (PrEP) for HIV</li>
	<li>Discuss and manage Post Exposure Prophylaxis (PEP) if you think you may have had a risk of exposure to HIV</li>
	<li>Manage chronic and recurring genital infections, for example genital herpes, recurrent candida (&ldquo;thrush&rdquo;), recurrent bacterial vaginosis (BV)</li>
	<li>Discuss and support psychosexual problems and concerns</li>
	<li>Deal with and treat <u>erectile dysfunction.</u></li>
</ul>

