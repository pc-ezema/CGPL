                        <h3 class='mt-4'></h3>
                        <h3><img alt="" src="../../images/images/Vaccination.jpg" style="width: 350px; height: 233px;" />Meningitis B Adult &amp; Paediatric Vaccine</h3>

<p>The cost of the vaccine is &pound;135.00</p>

<p>2 doses are required.&nbsp;</p>

<p>This Vaccine is recommended to be administered within a &pound;95.00 GP Consultation with comprehensive advice. However, you may wish to have the vaccination only without a consultation which costs &pound;25.00</p>

<p>Worrying about meningitis is normal, especially if you have children. While no vaccine is 100 percent effective, getting vaccinated against the main strains of meningitis is the best way to help prevention.</p>

<p>This Meningitis B vaccination protects against almost 90 percent of the strains of meningococcal group B bacteria (which is a cause of meningitis) in England.</p>

<p>The NHS offers a MenB vaccination as part of the routine childhood immunisations programme in the UK. Vaccinations are given to babies at the ages of two, four and 12 months. Vaccinating babies helps protect them when they&rsquo;re most at risk.&nbsp;</p>

<p>Meningitis B was added to the programme in 2015, for babies born on or after 1 July 2015. Those children and adults who fall outside of the NHS age range can consider having the vaccination privately at CPGL Sheffield .</p>

<p>Meningitis can affect anybody.&nbsp;It&rsquo;s most common in children, teenagers and young adults aged between 15 and 24.&nbsp;That&rsquo;s because these groups tend to socialise more at school and universities, which leads to the spreading of bacteria. Outbreaks can happen at any time though and to anybody. Around 20 percent of reported MenB cases are in people aged 45 and over.</p>

<p>Side effects are usually mild. The injected area may become sore and red.&nbsp;</p>

<p>Young children may get a temperature of over 37.5C (fever), so it&rsquo;s a good idea to check their temperature often with a thermometer. You can consider giving them age-appropriate paracetamol to help lower their temperature. Make sure they&rsquo;re in comfy clothes and keep them cool.</p>

<p>Headaches and joint pain are most common in older children and adults. These should go away with time, but can be eased with pain relief such as paracetamol.&nbsp;</p>

<p>Meningitis is a serious infection which causes the lining around the brain and spinal cord to become inflamed. It&rsquo;s usually caused by bacteria or a virus.&nbsp;Bacterial meningitis can lead to brain damage, deafness, blindness, epilepsy, blood poisoning (septicaemia) and even death.&nbsp;</p>

<p>Meningococcal bacteria are the most common cause of bacterial meningitis in the UK, and can lead to meningitis and blood poisoning (septicaemia). There are five main groups. Meningococcal group B (MenB) is the most common, accounting for over 54 percent of reported cases of meningococcal meningitis.</p>

<p>&nbsp;</p>
