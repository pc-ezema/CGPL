                        <h3 class='mt-4'></h3>
                        <h3><a href="../../images/images/Vaccination.jpg"><img alt="" src="../../images/images/Vaccination.jpg" style="width: 350px; height: 233px;" /></a>Diphtheria, Tetanus, Whooping Cough (pertussis) &amp; Polio Vaccine Paediatric</h3>

<p>The cost of the Vaccine is &pound;85.00&nbsp;</p>

<p>The Vaccine is administered in 3 separate doses.</p>

<p>This Vaccine is recommended to be administered within a &pound;95.00 GP Consultation with comprehensive advice. However, you may wish to have the vaccination only without a consultation which costs &pound;25.00</p>

<p>The 4-in-1 vaccine, also known as the DTaP/IPV or dTaP/IPV vaccine, helps protect your child against:</p>

<ul>
	<li>Diphtheria</li>
	<li>Tetanus</li>
	<li>Pertussis (whooping cough)</li>
	<li>Polio</li>
</ul>

<p>This vaccine is offered to children aged over 3 years and 4 months and is often administered at the same time as the MMR Vaccine.</p>

<p>Older children and adults can have this vaccine. This vaccine boots immunity from the initial vaccine given at 8,12 and 16 weeks of age.</p>

<p><strong><big>What&#39;s diphtheria?</big></strong></p>

<p>Diphtheria&#39;s a serious disease that usually begins with a sore throat and can quickly cause breathing problems. It can damage the heart and nervous system and, in severe cases, can be fatal.</p>

<p>Diphtheria can spread from person to person through close contact.</p>

<p><strong><big>What&#39;s tetanus?</big></strong></p>

<p>Tetanus is a disease affecting the nervous system that can lead to muscle spasms, cause breathing problems and even become fatal.</p>

<p>It&#39;s caused when bacteria in the soil and manure get into the body through open cuts or burns. Tetanus can&#39;t be passed from person to person.</p>

<p><big><strong>What&#39;s pertussis (whooping cough)?</strong></big></p>

<p>Whooping cough is a disease that can cause long bouts of coughing and choking, making it hard to breathe. Whooping cough can last for up to 10 weeks.</p>

<p>Babies under a year of age are most at risk from whooping cough. For these babies, the disease is very serious and can be fatal. It&#39;s not usually as serious in older children.</p>

<p>Whooping cough can be spread from person to person through close contact.</p>

<p><strong><big>What&#39;s polio?</big></strong></p>

<p>Polio&#39;s a virus that attacks the nervous system and can cause permanent paralysis of the muscles. If it affects the chest muscles or brain, polio can kill.</p>

<p>The polio virus is usually spread from person to person, or by swallowing contaminated food or water.</p>
