                        <h3 class='mt-4'>GP Consultations</h3>
                        <p dir="ltr">If you need to arrange an appointment with a GP but have found that you cannot secure an NHS appointment quickly, we have private GP consultations available.</p>

<p dir="ltr">&nbsp;</p>

<h2 dir="ltr">What Is A Private GP Consultation?</h2>

<p dir="ltr">A private GP consultation is an appointment in which a patient meets with a highly experienced and qualified GP to discuss any health concerns they may have. Unlike NHS GP consultations, which average<a href="https://pharmaceutical-journal.com/article/news/uk-has-shortest-gp-consultations-in-europe-study-finds#:~:text=The%20UK%20has%20the%20shortest,their%20GP%20during%20an%20appointment.https://pharmaceutical-journal.com/article/news/uk-has-shortest-g"> 9 minutes</a>, our private GP consultations are not rushed.&nbsp; Instead, our private GP consultations will last at least 30 minutes, providing an extensive review of your symptoms and condition. During your private GP consultation, you can expect to be listened to attentively, examined and diagnosed.&nbsp; Any necessary tests are also performed on the spot, eliminating the need to make additional appointments at a later date.</p>

<p dir="ltr">&nbsp;</p>

<h2 dir="ltr">What Can A Private GP Consultation Be Used For?</h2>

<p dir="ltr">Carried out by both male and female GP&rsquo;s, our private GP consultations are comprehensive:</p>

<p>We can issue prescriptions if necessary, and repeat prescriptions can be obtained without returning to your local GP<o:p></o:p></p>

<ul>
	<li>Everything is, of course, totally confidential</li>
	<li>Our GPs can perform diagnostics including blood tests, ECG and many other tests including sexual health, cancer screening, heart, &nbsp;and many other health checks.</li>
	<li>We can organise further tests, referral to a specialist, or offer a second opinion</li>
	<li>Furthermore, our GPs offer Vaccinations and Immunisations</li>
	<li>Our GPs offer taxi medicals and Visa Medicals and Adoption Medicals</li>
	<li>Private Sick notes</li>
	<li>Allergy Testing</li>
	<li>Covid 19 Antibody Tests and Swabs for travellers</li>
	<li>Sexual Health Testing</li>
	<li>Health Screening</li>
</ul>

<p dir="ltr">We provide high quality private GP services at an affordable price. If you&rsquo;re feeling poorly, have a health concern or want a second opinion, you may have a two week wait to see your own GP. We offer same day, rapid access, convenience all at an alternative and affordable price.</p>

<p>We&rsquo;re situated in the heart of Sheffield  on Park Avenue and open&nbsp; weekdays between 8.30am &ndash; 6.00pm.</p>

