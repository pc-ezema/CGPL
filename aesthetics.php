                        <h3 class='mt-4'></h3>
                        <p>At CPGL, our aesthetic procedures include consultations for various anti-ageing treatments that can assist you in combating the signs of wrinkles and ageing. All procedures are carried out by our Aesthetics Doctor and an Aesthetics trained registered Nurse.&nbsp;We can also offer advice surrounding our anti-ageing treatments throughout the duration of a consultation.&nbsp; Should anti-ageing treatment be deemed suitable for you following a consultation, our specialists will recommend treatments that can be carried out in our CCG approved multi-clinic.</p>

<p><strong>Our Aesthetic Services</strong></p>

<p><a href="aesthetics/anti-wrinkle-treatments.php">Anti-Wrinkle Treatments</a></p>

<p><a href="aesthetics/dermal-fillers.php">Derma Filers Treatments</a></p>

<p>Expression lines, such as&nbsp;wrinkles, are one of the first signs of ageing and more commonly occur in sun-exposed areas. On the face, wrinkles appear around the eyes and are often referred to as crow&rsquo;s feet. However, wrinkles can also appear around the mouth (smokers&rsquo; lines), the forehead and between the eyes (frown lines). Hyperactivity of the facial expression muscles can also cause facial wrinkles to appear. This is because repeatedly creasing the skin often results in the development of deep frown lines, forehead and crow&rsquo;s feet.</p>

<p>Through the employment of anti-ageing treatments, the appearance of these lines can be minimised.</p>

<p>Whether you are looking to delay the signs of ageing or wish to enhance your appearance, we welcome you to arrange a consultation to uncover the anti-ageing treatments available at our clinic. As the anti-ageing services we provide are tailored to the needs of each of our clients, we would highly recommend contacting us directly to find out more about our aesthetic services. In doing so, we can make recommendations based on your requirements, provide you with a provisional treatment price quote, and we can invite you to our clinic for a consultation with our&nbsp;Aesthetic specialist Doctors or Nurses.</p>

<p><strong>Contact Us Today</strong></p>

<p>If you would like to find out more about our anti-ageing treatments, we welcome you to contact us today to arrange a consultation with one of our Aesthetics Doctors or Nurses. Our aesthetic practitioners will discuss the various treatment options with you and ensure that you can make an informed decision. Should you find yourself looking for a particular service, please contact us directly to determine how we can assist you.</p>
                        
                         
                          
                        
