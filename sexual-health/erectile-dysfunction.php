                        <h3 class='mt-4'></h3>
                        <p>Erection problems are very common, especially in men over the age of 40. At whatever age it occurs we recommend that you make an appointment with our sexual health consultant to establish if there are underlying reasons and what can be done to help you.</p>

<p><strong>What cause erectile dysfunction?</strong></p>

<p>Most men fail to get or keep an erection at times. Some men also find their erections are not as hard as they have been previously. This can be caused by stress, tiredness, anxiety or drinking too much alcohol and it&rsquo;s nothing to worry about. However, if it happens more often then it could be a sign of a physical or emotional problem.</p>

<p><strong>What will happen at my appointment?</strong></p>

<p>Your sexual health consultant will talk to you about the problem, and consider all aspects of your medical health as well as your lifestyle and your relationships. They will carry out some basic health checks, for example blood pressure and they will examine you, including a genital examination. They may also carry out a prostate examination.</p>

<p><strong>What treatment is available?</strong></p>

<p>We are pleased to be able to offer courses of Emsella, which is a completely painless, non-surgical, non-invasive treatment. The treatment involves sitting on a specialised chair, fully clothed. A strong electromagnetic field is generated causing supramaximal contraction of the muscles. It also stimulates the body to create new muscle fibres and proteins.</p>

<p>The ongoing treatments for ED you will be recommended depends on the underlying cause of your erectile dysfunction.</p>

<ul>
	<li>If your blood pressure or cholesterol is high you may be recommended treatment for this</li>
	<li>If you have a hormone problem you will be advised about replacement therapy</li>
	<li>If your erectile dysfunction is caused as a side effect of a drug then you may be advised to take an alternative</li>
	<li>If your erectile dysfunction is thought to be due to an emotional problem you may be directed to psychosexual counselling</li>
	<li>Medicine such as sildenafil (Viagra) may also be recommended. We will talk to you about whether it is safe for you to take it.</li>
	<li>Your sexual health consultant may also refer you to another specialist, for example an endocrinologist if a hormone problem is found. If this is advised it will be discussed with you.</li>
	<li>You may be recommended a vacuum pump if this is thought to be the most suitable treatment.</li>
</ul>

<p>Many men who suffer from erectile dysfunction will benefit from seeing a sexual health consultant, and with their support and advice will return to an enjoyable and pleasurable sex life.</p>

<p>Consultations cost &pound;85.00 &ndash; we offer 6 days a week. There is no need for a referral from your GP.</p>

