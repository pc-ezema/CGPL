                        <h3 class='mt-4'></h3>
                        <h2>Human Immunodeficiency Virus (HIV)</h2>

<p>CGPL tests for HIV.</p>

<ul>
	<li>
	<p>10 day Early Detection which looks for the RNA of HIV I &amp; II costs &pound;140.00</p>
	</li>
	<li>
	<p>28 day HIV Duo test which looks for HIV I &amp; II antibody and the p24 antigen (which appears first) costs &pound;70.00</p>
	</li>
	<li>
	<p>Rapid HIV test with results in clinic costs &pound;50.00</p>
	</li>
</ul>

<p>You can choose to have HIV testing at CGPL with the Health Care Assistant, which costs &pound;25.00,&nbsp;our&nbsp;Sexual Health Consultant which costs &pound;85.00, or GP which costs &pound;95.00. If you choose to have your test with one of our Health Care Assistant&#39;s, then you will need a consultation with the GP or Sexual Health Consultant to have the results explained to you.</p>

<p><a href="combined-sexual-health-testing.php">HIV testing is also included in our combined STI multiple tests.</a></p>

<p><strong>What is it?</strong></p>

<p>HIV (Human Immunodeficiency Virus) is a retrovirus that can lead to Acquired Immunodeficiency Syndrome (AIDS), a condition in which the immune system begins to fail, leading to life-threatening infections we ordinarily would be able to fight naturally. Infection also may mean an increased risk of contracting other STIs.</p>

<p>There are two types of HIV, HIV-1 and HIV-2. In the United Kingdom, unless otherwise stated, the term &ldquo;HIV&rdquo; primarily refers to HIV-1.&nbsp; HIV-2 diagnosis is more unusual in the UK, but if diagnosed can be managed.&nbsp; Both types of HIV will damage the body by reducing the number of specific cells called CD4 cells.&nbsp; Your body relies on these cells to help it fight off infections and other diseases. The numbers of these CD4 cells are reduced by the HIV virus, so you are not able to fight off infections as you normally would be able to.&nbsp;</p>

<p>Knowing your HIV status can protect your health and the health of others. Regular testing and &nbsp;retesting is advised if you continue to be at risk. A repeat test is recommended with every partner change.</p>

<p>You cannot catch HIV from someone who is HIV positive, on treatment and has a fully controlled virus.</p>

<p><strong>How do you get it?</strong></p>

<p>Complacency about HIV plays a key role in HIV risk. Transmission occurs through bodily fluids. This can happen during vaginal or anal sex or through sharing needles (including injecting drug use, injecting steroids, using drugs during sex, and also needlestick accidents). Transmission can also occur from mother to baby either before or during birth or through breastfeeding.</p>

<p>HIV cannot be spread by toilet seats, door knobs, swimming pools, hot tubs, bath tubs, shared clothing, or eating utensils.</p>

<p>Screening of blood products for HIV has eliminated transmission through blood transfusions or infected blood products in the <a href="http://en.wikipedia.org/wiki/Developed_country" title="Developed country">developed world</a>.</p>

<p><strong>Symptoms</strong></p>

<p>The first symptoms of HIV can appear within six weeks of infection. After this many people can have no symptoms for years.</p>

<p>Common symptoms of HIV infection include:</p>

<ul>
	<li>
	<p>unintentional weight loss</p>
	</li>
	<li>
	<p>chronic diarrhoea</p>
	</li>
	<li>
	<p>skin rashes</p>
	</li>
	<li>
	<p>an increase in herpes ulcers or thrush infections in your mouth and genitals</p>
	</li>
	<li>
	<p>sweats, especially at night</p>
	</li>
	<li>
	<p>unusual tiredness</p>
	</li>
	<li>
	<p>nausea or loss of appetite</p>
	</li>
	<li>
	<p>swollen lymph glands in the neck, groin or armpits</p>
	</li>
</ul>

<p>These symptoms can all be caused by conditions other than HIV. However, if some of these symptoms are experienced, it is a good idea to get an HIV test, especially if the patient has engaged in any high risk activity, including:</p>

<ul>
	<li>
	<p>sex with a new partner</p>
	</li>
	<li>
	<p>sex without condoms</p>
	</li>
	<li>
	<p>sex with commercial sex workers</p>
	</li>
	<li>
	<p>injecting drugs (including drugs used during sex and performance enhancing drugs,</p>
	</li>
</ul>

<ul>
	<li>
	<p>sex with multiple partners,</p>
	</li>
	<li>
	<p>sex with someone who you know has HIV,</p>
	</li>
	<li>
	<p>received a blood transfusion or surgery outside of the UK</p>
	</li>
</ul>

<p><strong>Times: Incubation, results and re-test</strong></p>

<p>The incubation time and results turnaround time vary depending on which test is performed:</p>

<p>10 day HIV tests have an incubation period of 10 days.&nbsp; The results take three working days from when the sample is received in the laboratory.</p>

<p>28 day HIV tests have an incubation period of 28 days.&nbsp; The results take four hours from when the sample is received in the laboratory.</p>

<p>There are several rapid HIV tests available which have different incubation periods and different times taken to run the test.&nbsp; Our instant rapid HIV test is Alere which has a 26 day incubation period and takes 20 minutes to give an accurate reading.</p>

<p>Depending on which test you choose, and when your exposure was, you will be advised when best to retest to confirm the result. If in any doubt please discuss with our sexual health and HIV consultant to receive the most accurate advice.</p>

<p><strong>What to do if the test is positive</strong></p>

<p>There is currently no cure for HIV or AIDS. However, HIV is no longer considered to be life threatening if detected early and properly managed by specialists. People with HIV who take treatment will remain healthy and should have a normal life expectancy.</p>

<p>Treatment consists of highly active anti-retroviral therapy (HAART), and advances continue to be made improving both quality of life and prognosis.&nbsp; If you test positive for HIV you should have a repeat test and our HIV consultant will ensure you are seen in a specialist clinic.</p>

<p><strong>Recent exposure and Needle stick injuries</strong></p>

<p>If you think you might have been exposed to the HIV virus, then discussion with our sexual health and HIV consultant will allow you to establish the extent of the risk. Post Exposure Prophylaxis (PEP) may be recommended.</p>

<p>PEP is a course of HIV medication which you can take if you have been at risk of HIV infection. The course of HIV medication lasts 28 days and, if it is started within 72 hours, it may be able to prevent you becoming infected with HIV.</p>

<p>You can get PEP prescribed at CGPL, at a <a href="http://www.gmfa.org.uk/sex/clinics/index#londonclinics">sexual health clinic</a> or in the Accident &amp; Emergency (A&amp;E) department of most hospitals.&nbsp;</p>

<p><strong>Can I take any measures to stop HIV acquisition?</strong></p>

<p>There are many measures you can take to stop HIV acquisition. This may include:</p>

<ul>
	<li>
	<p>Use condoms during vaginal and anal sex</p>
	</li>
	<li>
	<p>Do not share needles. There are many needle exchange schemes</p>
	</li>
	<li>
	<p>If you think you have been exposed to HIV in the last 72 hours attend CGPL, sexual health clinics or A&amp;E and ask about Post Exposure Prophylaxis (PEP)</p>
	</li>
	<li>
	<p>If you feel you may be at ongoing risk of HIV acquisition, please talk to our sexual health and HIV consultant about Pre-Exposure Prophylaxis (PrEP). This is a daily or on demand medication that you can take to prevent HIV acquisition.</p>
	</li>
</ul>
