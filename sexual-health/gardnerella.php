                        <h2>Gardnerella</h2>

<p><strong>What is it?</strong></p>

<p>Gardnerella Vaginalis is a bacteria that can cause a condition known as Bacterial Vaginosis (BV) in women.&nbsp; BV is caused when the naturally occurring bacterial populations in the vagina become unbalanced, allowing the Gardnerella bacteria to multiply in large numbers. Once the populations of normal vaginal bacteria have been overtaken, symptoms of bacterial vaginosis can occur.</p>

<p>Although Gardnerella is often associated with bacterial vaginosis, it can be detected in women who do not have any signs of infection. Gardnerella bacteria can sometimes be detected in men. It is generally thought that the presence of Gardnerella in men does not pose any risk factors to the man and that under normal circumstances the man&rsquo;s body will eliminate these bacteria over time. However, contracting a Gardnerella infection may cause an increased risk of getting other Sexually Transmitted Infections (STIs).</p>

<p>A long term infection in pregnant women can cause premature rupture of membranes, premature labour and delivery, along with a range of other potentially dangerous complications.&nbsp; Gardnerella has also been associated with pelvic inflammatory disease.</p>

<p>CGPL GPs and Sexual Health Consultants offer Gardnerella testing. The cost of the test is &pound;75.00. The cost of the consultation is &pound;85.00</p>

<p><a href="combined-sexual-health-testing.php">Gardnerella&nbsp;is also included in our combined STI multiple tests.</a></p>

<p><strong>How do you get it?</strong></p>

<p>A frequent mode of transmission involves sexual contact, but it has not been definitively established that men can transmit a Gardnerella infection to women. Gardnerella and BV cannot be spread by toilet seats, door knobs, swimming pools, hot tubs, bath tubs, shared clothing, or eating utensils.</p>

<p><strong>Symptoms</strong></p>

<p>Gardnerella infections often have no symptoms. In those people that get symptoms, the following may occur:</p>

<p>Women:</p>

<ul>
	<li>
	<p>Vaginal discharge</p>
	</li>
	<li>
	<p>Vaginal irritation</p>
	</li>
	<li>
	<p>A &quot;fish like&quot; odour</p>
	</li>
	<li>
	<p>A long-term infection in pregnant women can cause premature rupture of membranes, preterm labour and delivery, along with a range of other potentially dangerous complications.</p>
	</li>
	<li>
	<p>Gardnerella has also been associated with Pelvic Inflammatory Disease (PID)</p>
	</li>
</ul>

<p>Men</p>

<ul>
	<li>
	<p>Penile itching or discomfort</p>
	</li>
	<li>
	<p>Burning sensations on urination</p>
	</li>
</ul>

<p><strong>How to test and samples</strong></p>

<p>CGPL test for Gardnerella with a first catch urine sample or vaginal swab.&nbsp;</p>

<p><strong>Times: Incubation, results and re-test</strong></p>

<p>Gardnerella has a two week incubation period.&nbsp; Results are available within five days of the sample being received in the laboratory. Regular retesting is advised if you continue to be at risk. A repeat test is recommended with every partner change.</p>

<p><strong>What to do if the test is positive</strong></p>

<p>Gardnerella is treated and cured with antibiotics.&nbsp; We ask that our sexual health consultant provides a private prescription to patients who test positive and wish to be treated privately. Patients&nbsp; should not have unprotected sex until the treatment has finished and the symptoms have gone away.</p>
