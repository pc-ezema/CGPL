                        <h3 class='mt-4'></h3>
                        <h2><strong>Herpes</strong></h2>

<p><strong>What is it?</strong></p>

<p>Herpes (Herpes Simplex) is a very common and highly infectious virus which has two types:</p>

<ul>
	<li>Herpes Simplex I (HSV I) - The most likely cause of cold sores, typically around the mouth. This virus can also be caught genitally through oral sex.</li>
	<li>Herpes Simplex II (HSV II) - The virus that typically causes sores around the genitals. It may also be caught around the mouth through oral sex.</li>
</ul>

<p>MUMS offer Herpes testing. The cost of the test is &pound;100.00. Sometimes we test blood, sometimes we test using swab. You will need a consultation with our Sexual Health Consultant. This costs &pound;85.00</p>

<p><a href="combined-sexual-health-testing.php">Herpes is also included in our combined STI multiple tests.</a></p>

<p>Cold sores are not considered an STI as infection can occur in many non-sexual ways. However, Herpes I is responsible for about half of &quot;genital herpes&quot; cases. Genital Herpes is typically transmitted through sexual contact. Up to 60% of people who have Genital Herpes are unlikely to know they are infected as their symptoms will be minimal or non-existent. They are capable of infecting partners through sex when the virus is present on the genitals but there is no visible sign.</p>

<p>Herpes can cause complications during pregnancy and labour which can be managed by a doctor or midwife. If you are pregnant and think you have Herpes (a new or old infection) you must see a doctor. There is an increased chance of recurrent symptoms during pregnancy. Some women who have previously been asymptomatic may notice symptoms for the first time whist pregnant.</p>

<p>It is important to treat Herpes outbreaks, as those who have it have an increased risk of getting other STIs, including HIV.</p>

<p><strong>How do you get it?</strong></p>

<p>The herpes virus is transmitted by touching an active sore.&nbsp; HSV I is very common as it can be transferred by kissing (mouth to mouth contact).&nbsp; Both virus types can be spread by any body contact including oral, vaginal and anal sex.&nbsp; Neither HSV I nor HSV II are spread by toilet seats, door knobs, swimming pools, hot tubs, bath tubs, shared clothing, or eating utensils.</p>

<p><strong>Symptoms</strong></p>

<p>Up to 60% of people who have HSV are unlikely to know they are infected as their symptoms will be minimal or non-existent. They are capable of infecting partners through sex when the virus is present on the genitals, causing pimples, blisters, little cuts, itchy or sore skin.&nbsp; For those people who do have symptoms when the virus is active, then these are usually visible blisters.&nbsp;</p>

<p><strong>Times: Incubation, results and re-test</strong></p>

<p>The Herpes blood test results will be received two days after the sample has been received by the laboratory.</p>

<p>The Swab Herpes test results will be received five days after the sample has been received by the laboratory.</p>

<p>The incubation time for the Herpes blood test is 4 to 6 weeks.&nbsp; There is no incubation for the other tests as they are testing for current infection and only if the patient has visible symptoms.</p>

<p>The Herpes virus can lie dormant for many years, and so it is not always possible to accurately know when an infection occurred and which previous partners are at risk.&nbsp; Regular testing is advised for Herpes.</p>

<p><strong>What to do if the test is positive</strong></p>

<p>Like several other infections, once it is caught, Herpes Simplex (HSV I and HSV II) stays in the body and you can get recurrent outbreaks.</p>

<p>Our sexual health consultant can discuss with you ways to decrease the frequency and severity of the outbreaks. There are anti-viral medications available (tablets for genital infections) which can help control the infection. In addition, there are lifestyle changes which can also help control the time between outbreaks and their severity. Do not have any form of sex when the lesions are present.</p>

<p><strong>How to test and samples</strong></p>

<p>We offer Herpes tests for both type I and type II and will tell the patient whether they have both, type I, type II or neither.</p>

<p>There are different ways to test for Herpes, and the best method of testing will be offered to you after discussion with our sexual health consultant. This may be either a blood test or a swab if there are symptoms present.</p>
