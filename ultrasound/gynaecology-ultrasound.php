<p><strong>About your gynaecological scan</strong></p>

<p>Initially you will &nbsp;be scanned via your tummy during which you will need to attend with a full bladder. After the tummy scan, you will be asked to empty your bladder in order to proceed with a vaginal or also known as an internal scan. This scan is not carried out if you have never been sexually active and the doctor will enquire before proceeding with the scan. The doctor will explain the scan findings during your appointment.</p>

<p>Please state at the time of booking if you would prefer a female sonographer. If the scan is performed by a male there will always be a female chaperone present in the room.</p>

<p><strong>What&rsquo;s included in your Gynaecology Scan</strong></p>

<p><strong>A Report:</strong> A detailed report confirming scan findings will be given to you on the day. If you require any further treatment your scan report should be given to your GP who can arrange the appropriate referral</p>

<h3>Other Scans Available</h3>

<p><strong>Ovarian &pound;185.00</strong><br />
This scan assess the size and shaps of the Uterus and the ovaries. This scan is used to detect ovarian pathology such as cysts and cancer. You will have a transabdominal scan first&nbsp; where a probe is placed over your lower tummy and an internal scan where the probe is placed in your vagina to have a closer look at the ovaries. &nbsp;</p>

<p><strong>Pelvis &pound;185.00</strong><br />
A pelvic ultrasound is a test that uses sound waves to make pictures of the organs inside your pelvis. In women, a pelvic ultrasound is used to view the following - Cervix, Uterus, ovaries, baldder.You will have a transabdominal scan first followed by an internal scan. This scan is used to detect a variety of pelvic pathology including fibroids, polyps, ovarian cysts, PCOS and severe endometriosis.</p>

<p><strong>Uterus &pound;185.00</strong><br />
A pelvic ultrasound is a noninvasive diagnostic exam that produces images that are used to assess organs and structures within the female pelvis. A pelvic ultrasound allows quick visualization of the female pelvic organs and structures including the uterus, cervix, vagina, fallopian tubes and ovaries.</p>

