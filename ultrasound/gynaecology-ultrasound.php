                        <h3 class='mt-4'>Gynaecological Scan</h3>
                        <p>CGPL provide a range of ultrasound diagnostic services to women with gynaecological problems.</p>

<p>You can either attend for an ultrasound with a sonographer who will report the findings to you. &nbsp;If you prefer to see a Consultant Gynaecologist for a ultrasound and consultation this is also available at CGPL. &nbsp;The Consultant Gynaecologist will be able to recommend treatments if necessary either within the NHS or private practice. &nbsp;Initially you will be asked to give a brief outline of your problem, the date of your last period (if relevant) and whether you have had any significant gynaecology or other medical problems in the past.</p>

