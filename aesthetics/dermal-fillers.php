                        <h3 class='mt-4'></h3>
                        <p><strong>CGPL clinic offers a host of aesthetic procedures by a team of highly qualified and experienced Specialists in CCG approved multi-clinic.</strong></p>

<p>The Hyaluronic Acid filler is injected into the skin to soften the appearance of wrinkles, lines and scars or into the lip to give a natural volume replacement for a fuller appearance and slow down the signs of ageing.</p>

<p>The filler products are sterile colourless gels composed of hyaluronic acid, a substance that occurs naturally in the body. Hyaluronic acid is an important component of the skin and serves important functions in creating volume in the tissue.</p>

<p><strong>Consultations for anti-wrinkle injections</strong></p>

<p>Consultations are carried out by highly trained clinicians in aesthetic medicine. Whilst there are a number of fillers available on the market, we use the market-leading established brands that have proven themselves to be safe over many years and of highest quality.&nbsp;</p>

<p>The procedure takes between 10-30 minutes to carry out. The discomfort is minimal (at the time of injection only) as very fine needles are used and local anaesthetic cream can be used.<span style="color: rgb(102, 102, 102); font-family: Poppins; font-size: 15px;">&nbsp;</span>The results are immediate and there is no recovery time, but there can be swelling and bruising after a lip filler for a few days. The most common areas treated are the lower face (including lips, cheeks, nasolabial folds, marionette lines) and scars.&nbsp;The effect of treatment generally last between 12 and 15months, but in the lips can be shorter around 6-9 months. The fillers we use are reversible.</p>

<p>We recommend using topical anaesthetic cream to keep the discomfort to the minimum. Local anaesthetic injection can be used too. Follow up&nbsp;if required is 2-3 weeks after treatment and a leaflet is provided.</p>
