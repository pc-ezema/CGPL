                        <h3 class='mt-4'>Early Pregnancy Scans</h3>
                        <p><strong>CGPL understands that the first few months of pregnancy can be quite an anxious time for many women with various concerns as to whether the pregnancy is developing normally.</strong></p>

<div>
<div>
<p>An Early Pregnancy Scan (sometimes also called a Viability Scan or Dating Scan) provides reassurance in this very early stage of your pregnancy. A pregnancy can be seen on scan from as early as 6 weeks from the first day of your last period (LMP) at which time we should be able to see a heartbeat.</p>

<p>If you are unsure of your dates or have irregular cycles we do ask that you have a scan two weeks after your first positive pregnancy test.</p>

<p>Early Pregnancy Scans are not routinely offered on the NHS but you can have one privately without affecting your NHS entitlement. Sometimes it is requested by a doctor or midwife to confirm that your pregnancy is healthy (in this case your scan is often called a viability scan) and to calculate the date when your baby is due (usually referred to as a dating scan). It can also be performed to confirm the results of a pregnancy test or just to provide you with some reassurance that all is progressing as it should be. At CGPL we are very proud of our professional experienced and caring staff who will ensure that you receive the best care possible and give advice and support where appropriate.</p>

<div class="wpb_text_column wpb_content_element " xss="removed">
<div class="wpb_wrapper">
<p><span xss="removed"><strong>The reasons for having an Early Pregnancy Scan are to:</strong></span></p>

<p>&ndash; Check the pregnancy sac is located within the uterus and rule out ectopic pregnancy.<br />
&ndash; Check there is a heartbeat present to confirm viability.<br />
&ndash; Calculate the gestation of pregnancy (number of weeks)<br />
&ndash; Determine whether it is a singleton or multiple pregnancy<br />
&ndash; Assess bleeding/spotting or any unusual pain</p>

<p>Early Pregnancy Scans are normally performed trans-abdominally (through your tummy) however an internal (trans-vaginal) scan may be required at earlier gestations (6-7 weeks) to obtain clearer images. This can be due to a number of reasons, for example, if your womb tilts backwards (retroverted) or it may be that you are less weeks pregnant than you calculated. We will always try to scan trans-abdominally first but if we need to do an internal scan then this will not be harmful in any way to the baby.&nbsp;We discuss the option of a vaginal scan if needed and only perform if necessary with your consent (no additional cost).</p>

<p><span xss="removed"><strong>What happens during an ultrasound scan?</strong></span></p>

<p>Ultrasound scans are safe and do not cause discomfort.&nbsp;Ultrasound gel is used on the abdomen and the ultrasound probe presses on your tummy to obtain images. If you have a very full bladder it may be a little uncomfortable. An ultrasound scan typically takes approximately 5-10 minutes to complete. If the images are unclear you will be offered a trans-vaginal ultrasound scan (internal). During your scan we will explain the findings to you.</p>

<p>Following your scan we will give you your detailed report and you will have the opportunity to ask any questions.&nbsp;Your scan will then be uploaded along with your images onto a USB memory stick to take home on the day.</p>

<p><span xss="removed"><strong>6 weeks</strong></span> &ndash; At this stage the pregnancy is of course small, but having an early pregnancy scan we should be able to see the gestation sac with a yolk sac developing in your uterus. We should be able to see and hear your baby&rsquo;s heartbeat on scan, which is very reassuring. If we cannot see a heartbeat then this is either due to the pregnancy being too early (this is the commonest reason) or that the pregnancy is found to be not viable. If a heartbeat is seen, the risk for miscarriage at this stage is reduced to approximately 10%.</p>

<p><span xss="removed"><strong>7 weeks</strong></span> &ndash; At this stage we can see a fetal heartbeat and the early separation of the embryo from the yolk sac. We look at the heartbeat and measure the Crown Rump Length (CRL), this means the measurements are taken from the top of the baby&rsquo;s head to its bottom. &nbsp;From this measurement we can work out how many weeks and days pregnant you are (this early measurement is approximate for dating). If a fetal heartbeat is present the risk for miscarriage at this stage is reduced to approximately 5%.</p>

<p><span xss="removed"><strong>8 weeks</strong></span> &ndash;&nbsp; &nbsp;At this stage often the baby can be seen making small movements (usually past 8 weeks) which are amazing to see. We again measure the CRL to check gestation.&nbsp;Your risk for having a miscarriage is now only 2% if the heartbeat is present.</p>

<p><span xss="removed"><strong>9-10 weeks</strong></span> &ndash; At this stage the baby is now much more recognisable as a developing baby.&nbsp;We again measure the CRL to check gestation.&nbsp;If the heartbeat is present and the baby measures appropriately for your dates, the risk for having a miscarriage now drops to 1%.</p>
