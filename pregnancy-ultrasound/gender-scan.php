                        <h3 class='mt-4'>Gender Scan</h3>
                        <p>Gender or sexing ultrasound scans are performed to determine the sex of your baby.</p>

<p>You must be at least 15 weeks by your NHS due date in order to book this scan. This is because from 15 weeks of pregnancy the accuary of a gender scan is&nbsp;around 99%. If upon scan you are less that 15 weeks we are unable to report the gender of your baby and you will be charged for the appointment.</p>

<p>As part of the gender scan we will also check the growth of your baby.</p>

<p>Your scan will be an abdominal scan. If we are unable to see clear views of the gender of your baby we may send you for a walk to try and change the position of the baby. If we are still unable to determine the gender on the day, we will arrange a repeat appointment for you free of charge.</p>

