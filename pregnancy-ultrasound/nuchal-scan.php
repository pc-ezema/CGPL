                        <h3 class='mt-4'>Nuchal Scan</h3>
                        <p><strong>A nuchal scan is a screening test for Downs Syndrome and two other chromosomal abnormalities, Edward&rsquo;s and Patau&#39;s Syndrome.</strong></p>

<div>
<p>Many NHS hospitals now offer a nuchal scan, but the NHS scan does not look for all of the markers for Downs syndrome that MUMS does. They do not look for the presence of a nasal bone, the heart and the blood flow studies. Therefore our detection rate is much higher than the NHS can offer as we are looking at the baby in more detail. All nuchal scans performed at MUMS are undertaken by a fetal medicine specialist, who is highly skilled in this screening process.</p>

<p>All women whatever their age, have a small risk of delivering a baby with Downs Syndrome; the risk increases as the age of the mother increases. These syndromes are chromosomal abnormalities caused by the presence of an extra chromosome within the cells of the baby.</p>

<p>The nuchal scan can only be performed between 11 weeks + 2 days and 13 weeks + 6 days of pregnancy and is carried out by fetal medicine specialist.</p>

<p>This test also includes the results of a blood test which is taken from the mother to measure the levels of two hormones that are being released directly by the pregnancy (free B-hCG and PAPP-A). This blood test can be taken from 10 weeks of pregnancy and when taken prior to the scan appointment ensures complete results are given on the day. If the blood test is taken on the day of the scan the final result will be available within two to three days.</p>

<div>
<p>At the time of the scan we measure the size of the baby in order to date the pregnancy accurately.</p>

<p>A detailed ultrasound assessment is then undertaken:</p>

<ul>
	<li>To measure the fluid under the skin at the back of baby&rsquo;s neck (nuchal translucency)</li>
	<li>To determine the baby&rsquo;s heart rate</li>
	<li>To assess the blood flow through the valve on the right side of baby&rsquo;s heart (Tricuspid Regurgitation)</li>
	<li>To assess the blood flow through a vessel close to baby&rsquo;s heart (Ductus Venosus)</li>
	<li>To assess the presence or absence of the baby&rsquo;s Nasal Bone</li>
</ul>

<p>The detailed ultrasound assessment also includes an anatomy survey of the entire baby as well as cord insertion to the baby and to the placenta.</p>

<p>These assessments, together with your age and the results of your blood test are combined together to give you a risk factor of the pregnancy being affected by Downs Syndrome as well as the other two chromosomal problems mentioned above.</p>

<p>A result of between 1:2 and 1:150 is considered to be high risk result. A low risk result does not exclude Downs Syndrome, but does mean the chance of your baby having Downs Syndrome is very small.</p>

<p>Parents will receive full counselling concerning the significance of these risks and the options for further testing.</p>

<p>The only way to know for sure whether your baby has a chromosomal abnormality or not is to have a diagnostic test.&nbsp; Either chorionic villus sampling (CVS) or Amniocentesis &ndash; these tests carry a risk of miscarriage of just under 1%.</p>

<p>Alternatively the Harmony or Panorama (NIPT) test may be your preferred option as there is no miscarriage risk.</p>

<p><strong>A Nuchal Scan includes:</strong></p>

